def run():
    with app.app_context():
        before = Product.query.count()
        added = 0
        updated_sketchfab = {}
        
        for p in NEW_PRODUCTS:
            # Check if product with same name exists
            existing = Product.query.filter_by(name=p["name"]).first()
            if existing:
                print(f"SKIP (exists): {p['name']}")
                continue
            
            sfab = p.pop("sketchfab", None)
            product = Product(
                name=p["name"],
                category=p["category"],
                price=p["price"],
                original_price=p.get("original_price"),
                stock=p["stock"],
                image_url=p["image_url"],
                description=p["description"],
                specs=p.get("specs", "{}"),
                ar_model=sfab,  # Fix Issue #2: save Sketchfab UID for 3D models
            )
            db.session.add(product)
            db.session.flush()  # get the ID
            updated_sketchfab[product.id] = sfab
            added += 1
            print(f"ADDED [{product.id}]: {product.name} ({product.category})")
        
        db.session.commit()
        after = Product.query.count()
        print(f"\n✅ Done! {before} → {after} products (+{after - before})")
        
        # Print sketchfab mapping for ar-viewer.js update
        print("\n--- Sketchfab mapping to add to ar-viewer.js ---")
        for pid, sfid in sorted(updated_sketchfab.items()):
            if sfid:
                print(f"    {pid}: '{sfid}',")

if __name__ == "__main__":
    run()
